/**
 * Enumeración que define las valoraciones de feedback disponibles.
 */
export enum FeedbackRating {
    /**
     * Valoración que indica que la interacción fue exitosa.
     */
    SUCCESS = "success",

    /**
     * Valoración que indica que hubo un error o equivocación en la interacción.
     */
    MISTAKE = "mistake",
}
